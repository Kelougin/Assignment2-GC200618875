package com.example.demo;

public class Item {
    private int id; //Variable for item id
    private String name; //Variable for name
    private String description; //Variable for description
    private String type; //Variable for type

    //Getter for id
    public int getId() {
        return id;
    }
    //Getter for name
    public String getName() {
        return name;
    }
    //Getter for description
    public String getDescription() {
        return description;
    }
    //Getter for type
    public String getType() {
        return type;
    }
}
